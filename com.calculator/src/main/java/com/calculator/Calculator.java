package com.calculator;

public class Calculator {
	public int sum(int a, int b) {
		int sum = a + b;
		return sum;
	}

	public int subtract(int a, int b) {
		int sub = a - b;
		return sub;
	}
	
	public int mul(int a, int b) {
		int mul = a * b;
		return mul;
	}
	
	public int divide(int a, int b) {
		int div = a / b;
		return div;
	}
}
